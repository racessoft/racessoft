package org.races.model;

public class SparesSaleBySE {
	String employeeId;
	String employeeName;
	String jobcardNumber;
	double totalBillAmt;

	public String getEmployeeId() {
		return employeeId;
	}

	public void setEmployeeId(String employeeId) {
		this.employeeId = employeeId;
	}

	public String getEmployeeName() {
		return employeeName;
	}

	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}

	public String getJobcardNumber() {
		return jobcardNumber;
	}

	public void setJobcardNumber(String jobcardNumber) {
		this.jobcardNumber = jobcardNumber;
	}

	public double getTotalBillAmt() {
		return totalBillAmt;
	}

	public void setTotalBillAmt(double totalBillAmt) {
		this.totalBillAmt = totalBillAmt;
	}

}
