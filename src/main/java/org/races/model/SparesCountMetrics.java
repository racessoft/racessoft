package org.races.model;

public class SparesCountMetrics {
	String sparesCode;
	String sparesDesc;
	int noOfItemsSold;
	String soldDate;

	public String getSparesCode() {
		return sparesCode;
	}

	public void setSparesCode(String sparesCode) {
		this.sparesCode = sparesCode;
	}

	public String getSparesDesc() {
		return sparesDesc;
	}

	public void setSparesDesc(String sparesDesc) {
		this.sparesDesc = sparesDesc;
	}

	public int getNoOfItemsSold() {
		return noOfItemsSold;
	}

	public void setNoOfItemsSold(int noOfItemsSold) {
		this.noOfItemsSold = noOfItemsSold;
	}

	public String getSoldDate() {
		return soldDate;
	}

	public void setSoldDate(String soldDate) {
		this.soldDate = soldDate;
	}

}
